public class NestedFor {
    public static void main(String[] args) {
        // All Multiplication Table from 1 to 100
        for(int i=1; i<=100; i++){

            for(int j= 1; j<=10; j++){
                System.out.println(i +" * "+j + " = "+(i*j));
            }
            System.out.println("\n");
        }
    }
}
// WAP IN JAVA TO FIND MULTIPLICATION TABLE FROM 100 To 1

