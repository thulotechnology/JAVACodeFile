public class ArrayWork1 {
    public static void main(String[] args) {
        // create an array
        String[] names = {"Anar", "Rama", "Rachana", "Ram", "Munu"};
        // accessing each array elements
        for (int i = 0; i < names.length; i++) {
            System.out.println("Name at index "+i+" " + names[i]);
        }

    }
}

